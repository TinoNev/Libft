#include "libft.h"

int		ft_strdigit(const char *s)
{
	int		i;

	if (!(s))
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		if (ft_isdigit(s[i]) == 0)
			return (0);
		++i;
	}
	return (1);
}
